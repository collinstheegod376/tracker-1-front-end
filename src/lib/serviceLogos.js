/**
 * Returns logo metadata for a given service name, or null for unknown services.
 * Case-insensitive lookup with fallback.
 */
export function getServiceMeta(serviceName) {
  if (!serviceName) return null;
  // Direct match
  if (SERVICE_META[serviceName]) return SERVICE_META[serviceName];
  // Case-insensitive match
  const lower = serviceName.toLowerCase();
  const key = Object.keys(SERVICE_META).find((k) => k.toLowerCase() === lower);
  return key ? SERVICE_META[key] : null;
}
